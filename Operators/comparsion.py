'''
a = 10
b = 12
1. ==	= (a == b) is not true.
2. !=	= (a != b) is true.
3. >	= (a > b) is not true.
4. <	= (a < b) is true.
5. >=	= (a >= b) is not true.
6. <=	= (a <= b) is true.
'''

# Comparison operators in Python

# Example:
x = 10
y = 12

print(x,id(x),type(x))

print(y,id(y),type(y))

# Output: x > y is False
print('x > y  is',x>y)

# Output: x < y is True
print('x < y  is',x<y)

# Output: x == y is False
print('x == y is',x==y)

# Output: x != y is True
print('x != y is',x!=y)

# Output: x >= y is False
print('x >= y is',x>=y)

# Output: x <= y is True
print('x <= y is',x<=y)
